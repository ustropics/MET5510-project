% FILENAME: cmap_managua.m
%
% DESCRIPTION: Diverging orange-to-cyan colormap.
%              Warm (positive) to cool (negative) — great for fluxes.
%              Interpolates between 10 sampled colors.
%
% INPUT:
%   n - Number of colors (default: 256)
%
% OUTPUT:
%   cmap - n x 3 RGB matrix (0–1 range)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% FUNCTION %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function cmap = cmap_managua(n)
    
    %% --------------------------------------------------------------------
    %% 1. Set default input
    %% --------------------------------------------------------------------
    if nargin < 1, n = 256; end

    %% --------------------------------------------------------------------
    %% 2. Define node colors (RGB, 0–1)
    %% --------------------------------------------------------------------
    % Hex: #ffcf67, #dd9a55, #bb6d46, #92463b, #672b3c,
    %      #4e315e, #4e5593, #5c81bd, #6db1de, #81e7ff
    nodes = [
        1.00000  0.81263  0.40424;
        0.86538  0.60323  0.33442;
        0.73223  0.42762  0.27616;
        0.57359  0.27302  0.23155;
        0.40507  0.16822  0.23590;
        0.30712  0.19370  0.37056;
        0.30601  0.33454  0.57733;
        0.35955  0.50763  0.74256;
        0.42932  0.69411  0.87156;
        0.50411  0.90708  0.99978
    ];

    %% --------------------------------------------------------------------
    %% 3. Interpolate to n colors
    %% --------------------------------------------------------------------
    pos = linspace(0, 1, size(nodes,1));
    cmap = interp1(pos, nodes, linspace(0, 1, n), 'pchip');
end